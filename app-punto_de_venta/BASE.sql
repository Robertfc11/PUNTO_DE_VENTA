CREATE TABLE NOTA(

	NOT_CVE_NOTA     INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	NOT_FECHA		 DATE NOT NULL,
	NOT_HORA		 TIME NOT NULL,
	NOT_MONTO		 FLOAT NOT NULL,
    NOT_METODO       VARCHAR(15) NOT NULL

);


CREATE TABLE ARTICULO_NOTA(

	ART_CVE_ARTICULO 	INT NOT NULL,
	NOT_CVE_NOTA  		INT NOT NULL,
    ARN_CANTIDAD 		INT NOT NULL,
    ART_PRECIO_VENTA 	FLOAT NOT NULL,

    FOREIGN KEY (ART_CVE_ARTICULO) REFERENCES ARTICULO(ART_CVE_ARTICULO),
    FOREIGN KEY (NOT_CVE_NOTA) REFERENCES NOTA(NOT_CVE_NOTA)
);

















